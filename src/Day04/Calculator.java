package Day04;

public class Calculator {

    public int multiply(int a, int b) {
        //return ((a * b) * 2);
        return (a * b);
    }

    public int divide(int a, int b) {
        return (a / b);
    }

    public int add(int a, int b) {
        return (a + b);
    }

    public int subtract(int a, int b) {
        return (a - b);
    }
}
